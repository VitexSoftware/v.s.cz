#!/usr/bin/env php
<?php

declare(strict_types=1);

/**
 * Export release articles from the DB into a Phinx data migration.
 *
 * Usage: php bin/generate_articles_migration.php
 * Output: db/migrations/<timestamp>_release_articles_data.php
 */

// Load credentials from /etc/vscz.env
foreach (file('/etc/vscz.env', FILE_IGNORE_NEW_LINES | FILE_SKIP_EMPTY_LINES) as $line) {
    if (!str_starts_with(trim($line), '#') && str_contains($line, '=')) {
        [$k, $v] = explode('=', $line, 2);
        $_ENV[trim($k)] = trim($v);
    }
}

$pdo = new PDO(
    sprintf('mysql:host=%s;port=%s;dbname=%s;charset=utf8mb4',
        $_ENV['DB_HOST'] ?? 'localhost',
        $_ENV['DB_PORT'] ?? 3306,
        $_ENV['DB_DATABASE'] ?? 'vscz',
    ),
    $_ENV['DB_USERNAME'] ?? 'vscz',
    $_ENV['DB_PASSWORD'] ?? '',
    [PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION],
);

$rows = $pdo->query(
    "SELECT title, text, DatCreate, author, language, source_url
       FROM news
      WHERE source_url IS NOT NULL
      ORDER BY DatCreate ASC, id ASC",
)->fetchAll(PDO::FETCH_ASSOC);

if (!$rows) {
    echo "No release articles found (source_url IS NULL for all rows).\n";
    exit(0);
}

$count     = count($rows);
$timestamp = date('YmdHis');
$className = 'ReleaseArticlesData';
$outFile   = __DIR__."/../db/migrations/{$timestamp}_release_articles_data.php";

// Build the PHP rows array literal using var_export for safe quoting
$rowsPhp = '';
foreach ($rows as $row) {
    $rowsPhp .= "        [\n";
    foreach ($row as $col => $val) {
        $exported = var_export($val, true);
        $rowsPhp .= "            '{$col}' => {$exported},\n";
    }
    $rowsPhp .= "        ],\n";
}

$migration = <<<PHP
<?php

declare(strict_types=1);

use Phinx\Migration\AbstractMigration;

/**
 * Data migration: {$count} pre-generated release articles (EN + CS).
 * Generated by bin/generate_articles_migration.php on {$timestamp}.
 *
 * Rollback deletes only rows with source_url pointing to GitHub releases.
 */
final class {$className} extends AbstractMigration
{
    public function up(): void
    {
        \$rows = [
{$rowsPhp}        ];

        // Insert in chunks to stay under max_allowed_packet
        foreach (array_chunk(\$rows, 50) as \$chunk) {
            \$this->table('news')->insert(\$chunk)->saveData();
        }
    }

    public function down(): void
    {
        \$this->execute(
            "DELETE FROM news WHERE source_url LIKE 'https://github.com/VitexSoftware/%'",
        );
    }
}
PHP;

file_put_contents($outFile, $migration);

echo "Generated: {$outFile}\n";
echo "Rows:      {$count}\n";
echo "File size: ".number_format(filesize($outFile) / 1024, 1)." KB\n";
